////////////////////////////////////////////////////////
// Assignment statement
//
// The action does not have a visible effect.
//
// The action taken does have a permanent
// effect: for the rest of the execution of
// the program there will be a variable with value 5.
const totalPrice = 5;

////////////////////////////////////////////////////////
// A statement containing a method call.
//
// The visible effect is that the number 5
// gets displayed in the console.
//
// The action taken has no permanent effect.
console.log(totalPrice);

////////////////////////////////////////////////////////
// Each of the following lines is a statement.
//
// The actions taken have no visible effect.
//
// The actions taken have no permanent effect.
10;
3 + 5;
("hello");
`The total price is ${totalPrice}`;
